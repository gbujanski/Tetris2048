export const getColor = (value: number): { bg: string, text: string } => {
  if (value < 4) return { bg: '#776e65', text: '#000' };
  if (value < 8) return { bg: '#eee4da', text: '#000' };
  if (value < 16) return { bg: '#ede0c8', text: '#000' };
  if (value < 32) return { bg: '#f2b179', text: '#000' };
  if (value < 64) return { bg: '#f59563', text: '#000' };
  if (value < 128) return { bg: '#f67c5f', text: '#000' };
  if (value < 256) return { bg: '#f65e3b', text: '#000' };
  if (value < 512) return { bg: '#edcf72', text: '#000' };
  if (value < 1024) return { bg: '#edcc61', text: '#000' };
  if (value < 2048) return { bg: '#edc850', text: '#000' };
  if (value < 4096) return { bg: '#e8b026', text: '#000' };
  if (value < 8192) return { bg: '#a86ae4', text: '#000' };
  if (value < 16384) return { bg: '#6b4fc4', text: '#ffffff' };
  if (value < 32768) return { bg: '#3c3a32', text: '#ffffff' };
  if (value < 65536) return { bg: '#2b2b2b', text: '#ffffff' };
  if (value < 131072) return { bg: '#1e1b2e', text: '#ffffff' };
  if (value < 262144) return { bg: '#291b1b', text: '#ffffff' };
  if (value < 524288) return { bg: '#1e2d1e', text: '#ffffff' };
  if (value < 1048576) return { bg: '#2e1e2d', text: '#ffffff' };
  if (value < 2097152) return { bg: '#1c1c2e', text: '#ffffff' };
  if (value < 4194304) return { bg: '#2e2c1c', text: '#ffffff' };
  if (value < 8388608) return { bg: '#1f1f1f', text: '#ffffff' };
  if (value < 16777216) return { bg: '#181818', text: '#ffffff' };
  if (value < 33554432) return { bg: '#141414', text: '#ffffff' };
  if (value < 67108864) return { bg: '#101010', text: '#ffffff' };
  if (value < 134217728) return { bg: '#0d0d0d', text: '#ffffff' };
  if (value < 268435456) return { bg: '#0a0a0a', text: '#ffffff' };
  if (value < 536870912) return { bg: '#070707', text: '#ffffff' };
  if (value < 1073741824) return { bg: '#050505', text: '#ffffff' };
  return { bg: '#030303', text: '#ffffff' };
};
