export const getColor = (value: number): string => {
  if (value < 4) return '#776e65';
  if (value < 8) return '#eee4da';
  if (value < 16) return '#ede0c8';
  if (value < 32) return '#f2b179';
  if (value < 64) return '#f59563';
  if (value < 128) return '#f67c5f';
  if (value < 256) return '#f65e3b';
  if (value < 512) return '#edcf72';
  if (value < 1024) return '#edcc61';
  if (value < 2048) return '#edc850';
  if (value < 4096) return '#e8b026';
  if (value < 8192) return '#a86ae4';
  if (value < 16384) return '#6b4fc4';
  if (value < 32768) return '#3c3a32';
  if (value < 65536) return '#2b2b2b';
  if (value < 131072) return '#1e1b2e';
  if (value < 262144) return '#291b1b';
  if (value < 524288) return '#1e2d1e';
  if (value < 1048576) return '#2e1e2d';
  if (value < 2097152) return '#1c1c2e';
  if (value < 4194304) return '#2e2c1c';
  if (value < 8388608) return '#1f1f1f';
  if (value < 16777216) return '#181818';
  if (value < 33554432) return '#141414';
  if (value < 67108864) return '#101010';
  if (value < 134217728) return '#0d0d0d';
  if (value < 268435456) return '#0a0a0a';
  if (value < 536870912) return '#070707';
  if (value < 1073741824) return '#050505';
  return '#030303';
};
